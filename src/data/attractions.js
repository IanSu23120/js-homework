export function getAttractionsForDestination(destination) {
  const city = getCityByDestination(destination);
  if (!city) return attractions;

  const matches = attractions.filter((attraction) => {
    return attraction.city === city.value;
  });

  return matches.length > 0 ? matches : attractions;
}
